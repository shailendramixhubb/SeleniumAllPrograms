package java_cod_Harry;

public class Data_Type_of_Expressions_Increment_Decrement_Operators {
	public static void main(String[] args) {
		
		int a=6+10;
		System.out.println(a);
		
		float b=7+10.7f;
		System.out.println(b);
		
		float c=a%b;
		System.out.println(c);
		
		int d=4;
		d+=5;
		System.out.println(d);
		
		// comparision operator
		System.out.println(6==8);
		System.out.println(6==6);
		System.out.println(6>8);
		System.out.println(6<8);
		
		// logical operators
		
		System.out.println( 6==6 && 6==8);
		
		System.out.println(6>4 && 6>2);
		
		System.out.println(5>3 || 5>8);
		
		// Bitwise operators
		
		
		
	}

}
